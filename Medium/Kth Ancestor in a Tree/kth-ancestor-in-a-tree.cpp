int solve(Node *root, int &k, int node){
    if (root == NULL) {
        return -1; // Node not found, return -1
    }

    if (root->data == node) {
        return root->data;
    }

    int left = solve(root->left, k, node);
    int right = solve(root->right, k, node);

    if (left != -1 && right == -1) {
        k = k - 1;
        if (k <= 0) {
            k = INT_MAX; // Locking the ans;
            return root->data;
        }
        return left;
    }

    if (left == -1 && right != -1) {
        k = k - 1;
        if (k <= 0) {
            k = INT_MAX; // Locking the ans;
            return root->data;
        }
        return right;
    }

    return -1; // Target node not found in the current subtree
}

int kthAncestor(Node *root, int k, int node)
{
    int ans = solve(root, k, node);
    if (ans == -1 || ans == node) {
        return -1;
    }
    else {
        return ans;
    }
}

